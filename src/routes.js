const express = require("express");
const router = express.Router();
const projects = [];

const checkProjectExists = (req, res, next) => {
  const { id } = req.params;

  const project = projects.find(p => p.id == id);

  if (!project) {
    return res.status(400).json({ error: "Project not found" });
  }

  return next();
};

router.post("/projects", async (req, res) => {
  const { id, title } = req.body;

  const project = {
    id,
    title,
    tasks: []
  };

  projects.push(project);

  return res.json(project);
});

router.post("/projects/:id/tasks", checkProjectExists, async (req, res) => {
  const { id } = req.params;
  const { title } = req.body;

  const project = projects.find(p => p.id == id);

  project.tasks.push(title);

  return res.json(project);
});

router.get("/projects", async (req, res) => {
  return res.json(projects);
});

router.put("/projects/:id", checkProjectExists, async (req, res) => {
  const { id } = req.params;
  const { title } = req.body;

  const project = projects.find(p => p.id == id);

  project.title = title;

  return res.json(project);
});

router.delete("/projects/:id", checkProjectExists, async (req, res) => {
  const { id } = req.params;

  const projectIndex = projects.findIndex(p => p.id == id);

  projects.splice(projectIndex, 1);

  return res.send();
});

module.exports = router;
